<?php

namespace braga\tools\process;

class EventProcess
{
	// -----------------------------------------------------------------------------------------------------------------
	public function __construct(public bool $handled = false)
	{
	}
	// -----------------------------------------------------------------------------------------------------------------
	public function isHandled(): bool
	{
		return $this->handled;
	}
	// -----------------------------------------------------------------------------------------------------------------
	public function setHandled(bool $handled): void
	{
		$this->handled = $handled;
	}
	// -----------------------------------------------------------------------------------------------------------------
}